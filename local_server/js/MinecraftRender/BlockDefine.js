export function QueryBlockRender(Block) {
    let Block_ID = Block.identifier
    let Block_State = Block.states

    if (Block_ID in BlockIDTransfor.block_id) 
      Block_ID = BlockIDTransfor.block_id[Block_ID]
    
    let UpgradeID = false
    for (const [new_id, old_data] of Object.entries(BlockIDTransfor.block)) {
      if (old_data.block_id !== Block_ID) continue
      if (Object.keys(old_data.block_data).length === 0) UpgradeID = true
      for (const [statekey, stateval] of Object.entries(old_data.block_data)) {
        UpgradeID = false
        if (!(statekey in Block_State)) continue
        if (stateval !== Block_State[statekey]) continue
        UpgradeID = true
      }
      if (!UpgradeID) continue
      Block_ID = new_id
      break
    }

    if (Block_ID in StateToBlockDefineMap && Object.keys(Block_State).length) {
        const state = Object.keys(StateToBlockDefineMap[Block_ID])[0]
        const states_dict = StateToBlockDefineMap[Block_ID][state]
        if (state in Block_State) Block_ID = states_dict[`${Block_State[state]}`]
    }

    if (Block_ID in BlockDefine) return BlockDefine[Block_ID]
    else {return DefaultRenderData}
}