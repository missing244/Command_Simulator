def ResolveBlockColor(block_id:str, states:dict) :
    name = block_id.lower().replace('minecraft:', '')

    color_state = None
    if hasattr(states, 'get') :
        color_state = states.get('color')
        if not color_state :
            color_state = states.get('wood_type')
        if not color_state :
            color_state = states.get('stone_slab_type')
    elif hasattr(states, '__getitem__') :
        try : color_state = states['color']
        except (KeyError, TypeError) : pass
        if not color_state :
            try : color_state = states['wood_type']
            except (KeyError, TypeError) : pass
        if not color_state :
            try : color_state = states['stone_slab_type']
            except (KeyError, TypeError) : pass

    if color_state : name = f"{color_state}_{name}"
    if name in BLOCK_COLORS : return (*BLOCK_COLORS[name], 1)

    for key in sorted(BLOCK_COLORS.keys(), key=len, reverse=True) :
        if key in name : return (*BLOCK_COLORS[key], 1)

    return (0, 0, 0, 0)

